        if(this.currentIndex==1)this.$refs.searchList.refresh()
        
      })
    },
    hide() {
      this.showFlag = false;
    },
    search(query){
      this.query=query 
    },
    selectSuggest(){
      this.saveSearch()
    },
    switchItem(index){
      this.currentIndex=index
    },
    selectSong({ list, index }){
      console.log(list, index)
      if(index!=0){
        this.insertSong(list)
      }
    },
    ...mapActions(["insertSong"])
